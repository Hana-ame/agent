def example_sender_usage():
    """
    【完整调用示例】展示 Payload 模式、Context 模式、动态字段更新
    """
    import tempfile
    import os
    
    # 模拟创建临时配置环境
    temp_dir = Path(tempfile.mkdtemp())
    
    # 创建 payload.json
    payload_path = temp_dir / "payload.json"
    payload_config = {
        "model": "gpt-4",
        "temperature": 0.7,
        "messages": [
            {"role": "system", "content": "你是代码助手"}
        ],
        "_context": str(temp_dir / "system_prompt.json"),
        "_version": "1.0"
    }
    with open(payload_path, 'w') as f:
        json.dump(payload_config, f)
    
    # 创建 Context 外部文件
    context_path = temp_dir / "system_prompt.json"
    context_messages = [
        {"role": "system", "content": "长篇幅系统提示..." * 10},
        {"role": "user", "content": "示例用户输入"},
        {"role": "assistant", "content": "示例助手回复"}
    ]
    with open(context_path, 'w') as f:
        json.dump(context_messages, f)
    
    # 模拟 Requester（实际应导入真实类）
    class MockRequester:
        def __init__(self):
            self.last_metadata = None
        def send_request(self, body, **kwargs):
            print(f"[MockRequester] 收到请求: {len(body.get('messages', []))} 条消息")
            self.last_metadata = kwargs.get('context_metadata')
            # 模拟响应
            class MockResp:
                status_code = 200
            return MockResp()
    
    # ── 初始化 Sender ─────────────────────────────────────────────────────────
    
    def on_change(key, val, full):
        print(f"[回调] {key} 变更为 {val!r}")
    
    requester = MockRequester()
    sender = JsonPayloadSender(
        requester,
        str(payload_path),
        on_payload_change=on_change
    )
    
    print(f"\n初始化状态: {sender!r}")
    
    # ── 场景 1: Payload 模式（默认）────────────────────────────────────────────
    print("\n" + "="*50 + " 场景 1: Payload 模式 " + "="*50)
    
    result = sender.send_request_with_messages([
        {"role": "user", "content": "你好"}
    ])
    print(f"结果: success={result.success}, "
          f"base={result.base_message_count}, "
          f"dynamic={result.dynamic_message_count}")
    print(f"最终请求体含字段: {list(result.final_payload.keys())}")
    # 验证 _version 被清理
    assert '_version' not in result.final_payload
    
    # ── 场景 2: Context 模式 ──────────────────────────────────────────────────
    print("\n" + "="*50 + " 场景 2: Context 模式 " + "="*50)
    
    result = sender.send_request_with_messages(
        dynamic_messages=[{"role": "user", "content": "写个快速排序"}],
        use_context=True,
        request_metadata={"trace_id": "req-123", "user_id": "u-456"}
    )
    print(f"Context 模式结果: {result.base_message_count} 基础 + "
          f"{result.dynamic_message_count} 动态")
    # 验证 metadata 透传
    print(f"透传的 metadata: {requester.last_metadata}")
    
    # ── 场景 3: 动态字段更新 ──────────────────────────────────────────────────
